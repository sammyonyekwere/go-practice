package main

import (
	"fmt"
)

func main() {
	// Boolean
	// returns True/False
	a := 12
	b := 62
	c := 62
	// This will return false sice a is not equal to b
	fmt.Println(a == b)
	// This will return true since a is equal to c
	fmt.Println(b == c)

	// Intrgers and Floating point numbers
	// Integers do not have decimal paces
	// Floating point numbers have decimal places
	// Intergers are denoted generally by int
	// While floating point numbers are denoted by float64
	var x int = 64
	var y float64 = 33.3

	fmt.Println(x)
	fmt.Println(y)

}
